package com.intelizign.dmgcc.pojoclass;

public class SLABillingCycle {
	private Boolean month;
	private Boolean quarter;
	private Boolean half;
	private Boolean year;
	private Boolean once;

	public Boolean getQuarter() {
		return quarter;
	}

	public void setQuarter(Boolean quarter) {
		this.quarter = quarter;
	}

	public Boolean getHalf() {
		return half;
	}

	public void setHalf(Boolean half) {
		this.half = half;
	}

	public Boolean getYear() {
		return year;
	}

	public void setYear(Boolean year) {
		this.year = year;
	}

	public Boolean getOnce() {
		return once;
	}

	public void setOnce(Boolean once) {
		this.once = once;
	}

	public Boolean getMonth() {
		return month;
	}

	public void setMonth(Boolean month) {
		this.month = month;
	}

	public SLABillingCycle() {
		super();
	}

}
